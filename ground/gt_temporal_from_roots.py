"""
gt_temporal_from_roots.py — Generate proper gt_temporal.jsonl from GTBuilder output.

The EXISTING gt_temporal.jsonl was generated by dumping raw OSV `introduced` events,
which produces:
  - 73% entries with version "0" (OSV's "from the beginning" marker)
  - 10% entries with git commit hashes (GIT range events, not version numbers)
  - Empty version_sequence for ALL entries
  - Null timestamps for ALL entries
  - Massive duplication: 5981 rows from 79 CVEs (each affected[] entry → one row)

This script generates proper GT by:
  1. Using GTBuilder.build_root_causes() which resolves versions through the graph
  2. Converting RootCause objects to temporal GT format
  3. Building version sequences from the dependency graph
  4. Populating timestamps from graph node attributes
  5. Handling "introduced: 0" by mapping to earliest known graph version
  6. Deduplicating properly
  7. Computing real time_lag_days between origin and discovered versions

Usage:
    python gt_temporal_from_roots.py \\
        --dep-graph data/dep_graph_cve.pkl \\
        --cve-meta data/cve_records_for_meta.pkl \\
        --output data/gt_temporal_fixed.jsonl \\
        [--raw-osv-fallback]  # also include filtered raw OSV entries for coverage
"""

