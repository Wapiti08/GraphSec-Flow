def main():
    parser = argparse.ArgumentParser(
        description="Generate proper gt_temporal.jsonl from GTBuilder or OSV records"
    )
    parser.add_argument('--dep-graph', required=True, help="Dependency graph pickle")
    parser.add_argument('--cve-meta', default=None, help="CVE metadata pickle")
    parser.add_argument('--roots', default=None, help="GTBuilder root_causes.jsonl (if already built)")
    parser.add_argument('--output', required=True, help="Output JSONL path")
    parser.add_argument('--raw-osv-fallback', action='store_true',
                        help="Also include entries from raw OSV parsing")
    parser.add_argument('--stats', default=None, help="Output stats JSON")

    args = parser.parse_args()

    t0 = time_module.time()

    # --- Load graph ---
    print("Loading dependency graph...")
    with open(args.dep_graph, 'rb') as f:
        graph = pickle.load(f)
    print(f"  {graph.number_of_nodes()} nodes, {graph.number_of_edges()} edges")

    # --- Build package index ---
    print("Building package index...")
    pkg_index = build_package_index(graph)
    print(f"  {len(pkg_index)} package identifiers")

    # Show coverage
    sample_pkgs = sorted(pkg_index.keys())[:15]
    print(f"  Sample: {sample_pkgs}")
    total_versions = sum(len(v) for v in pkg_index.values())
    print(f"  Total version entries: {total_versions}")

    # --- Generate GT entries ---
    all_entries = []

    # Strategy 1: From pre-built root_causes.jsonl
    if args.roots:
        print(f"\nGenerating GT from root_causes.jsonl...")
        root_entries = gt_from_root_causes(args.roots, pkg_index)
        print(f"  Generated {len(root_entries)} entries from root causes")
        all_entries.extend(root_entries)

    # Strategy 2: From raw OSV records in cve_meta
    if args.cve_meta and (args.raw_osv_fallback or not args.roots):
        print(f"\nGenerating GT from CVE metadata (OSV records)...")
        with open(args.cve_meta, 'rb') as f:
            cve_meta = pickle.load(f)
        print(f"  Loaded {len(cve_meta)} CVE metadata entries")

        osv_entries = gt_from_osv_records(cve_meta, pkg_index)
        print(f"  Generated {len(osv_entries)} entries from OSV records")

        # Merge (dedup against existing)
        existing_keys = set((e['cve_id'], e['origin_version']) for e in all_entries)
        new = [e for e in osv_entries if (e['cve_id'], e['origin_version']) not in existing_keys]
        print(f"  {len(new)} new entries after dedup with root_causes")
        all_entries.extend(new)

    # --- Final deduplication ---
    seen = set()
    deduped = []
    for e in all_entries:
        key = (e['cve_id'], e['origin_version'])
        if key not in seen:
            seen.add(key)
            deduped.append(e)
    all_entries = deduped

    # --- Quality stats ---
    has_seq = sum(1 for e in all_entries if e.get('version_sequence'))
    has_ts = sum(1 for e in all_entries if e.get('origin_timestamp'))
    unique_cves = len(set(e['cve_id'] for e in all_entries))
    unique_pkgs = len(set(e['package'] for e in all_entries))

    print(f"\n{'='*60}")
    print(" GT TEMPORAL SUMMARY ".center(60, '='))
    print(f"{'='*60}")
    print(f"  Total entries:              {len(all_entries)}")
    print(f"  Unique CVEs:                {unique_cves}")
    print(f"  Unique packages:            {unique_pkgs}")
    print(f"  With version_sequence:      {has_seq}/{len(all_entries)}")
    print(f"  With origin_timestamp:      {has_ts}/{len(all_entries)}")
    print(f"  Time: {time_module.time() - t0:.1f}s")
    print(f"{'='*60}")

    # --- Write output ---
    Path(args.output).parent.mkdir(parents=True, exist_ok=True)
    with open(args.output, 'w') as f:
        for entry in all_entries:
            f.write(json.dumps(entry) + '\n')
    print(f"\n✓ Written to {args.output}")

    # --- Stats ---
    if args.stats:
        stats = {
            'total_entries': len(all_entries),
            'unique_cves': unique_cves,
            'unique_packages': unique_pkgs,
            'with_version_sequence': has_seq,
            'with_origin_timestamp': has_ts,
            'methods': dict(defaultdict(int, {
                e['method']: sum(1 for x in all_entries if x['method'] == e['method'])
                for e in all_entries
            })),
        }
        with open(args.stats, 'w') as f:
            json.dump(stats, f, indent=2)
        print(f"✓ Stats saved to {args.stats}")


if __name__ == '__main__':
    main()